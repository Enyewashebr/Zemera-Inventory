package com.zemera.inventory.model;

public class OrderItem {

    private Long id;
    private Long productId;
    private double quantity;
    private double unitPrice;
    private double totalPrice;

    private String productName; // optional, for ticket printing
    private String unit;        // optional, for ticket printing

    // ===== Getters & Setters =====

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getProductId() {
        return productId;
    }

    public void setProductId(Long productId) {
        this.productId = productId;
    }

    public double getQuantity() {
        return quantity;
    }

    public void setQuantity(double quantity) {
        this.quantity = quantity;
    }

    public double getUnitPrice() {
        return unitPrice;
    }

    public void setUnitPrice(double unitPrice) {
        this.unitPrice = unitPrice;
    }

    public double getTotalPrice() {
        return totalPrice;
    }

    // Optional: auto-calculate total price when setting quantity or unit price
    public void calculateTotalPrice() {
        this.totalPrice = this.quantity * this.unitPrice;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }
}
